package b;

public class App3 {

	public static void main(String[] args) {
		// integers
		byte x1 = 5;
		short x2 = 10;
		int x3 = 15;
		long x4 = 999999999999L;
		
		// decimals
		float f1 = 5.3F;
		double f2 = 2D;
		
		// characters
		char c1 = 'A';
		char c2 = '3';
		char c3 = ' ';
		char c4 = ',';
		char c5 = 1488; // unicode value
		
		System.out.println(c1);
		System.out.println(c2);
		System.out.println(c3);
		System.out.println(c4);
		System.out.println(c5);
		
		// boolean
		boolean b1 = true;
		boolean b2 = false;
		boolean b3 = 10 < 3;
		
		System.out.println(b1);
		System.out.println(b2);
		System.out.println(b3);
		
		
		
	}

}
